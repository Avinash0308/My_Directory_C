//Code for showing functionality of relational operators
#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter the value a\n");
    scanf("%d",&a);
    printf("Enter the value b\n");
    scanf("%d",&b);
    printf("a==b %d\n", a==b);
    printf("a!=b %d\n", a!=b);
    printf("a>b %d\n", a>b);
    printf("a<b %d\n", a<b);
    printf("a>=b %d\n", a>=b);
    printf("a<=b %d\n", a<=b);
    return 0;
}
