//Code for understanding all arithmetic operations
#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter value a\n");
    scanf("%d", &a);
    printf("Enter value b\n");
    scanf("%d",&b);
    printf("Modulus of given number is %d\n", a%b);
    printf("Addition of given number is %d\n", a+b);
    printf("Difference of given number is %d\n", a-b);
    printf("Division of given number is %d\n", a/b);
    printf("Multiplication of given number is %d\n", a*b);
    return 0;
}
